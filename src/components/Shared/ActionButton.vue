<template>
  <button :class="buttonClass">
    {{ text }}
  </button>
</template>

<script>
import { computed } from "vue";

export default {
  name: "ActionButton",
  props: {
    text: {
      type: String,
      default: "",
      required: true,
    },
    type: {
      type: String,
      default: "primary",
      required: false,
      validator(value) {
        return ["primary", "secondary"].includes(value);
      },
    },
  },
  setup(props) {
    const buttonClass = computed(() => {
      return {
        [props.type]: true,
      };
    });

    return { buttonClass };
  },
  // computed: {
  //   buttonClass() {
  //     return {
  //       [this.type]: true,
  //     };
  //   },
  // },
};
</script>

